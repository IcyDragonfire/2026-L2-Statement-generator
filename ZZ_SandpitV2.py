# list experiment

fruit_list = ['apple', 'apple2', 'apple3', 'apple4']

for item in fruit_list:

    print()

    # print the whole word...
    print("Fruit Name: ", item)

    # print the first letter...
    print("First letter", item[0])

    # print first few letters...
    print("First 5 letters", item[:5])


    # print whole word...
    print("entire word", item[:6])
